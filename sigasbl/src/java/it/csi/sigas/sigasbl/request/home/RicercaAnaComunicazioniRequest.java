package it.csi.sigas.sigasbl.request.home;

import java.io.Serializable;

public class RicercaAnaComunicazioniRequest implements Serializable{

	private static final long serialVersionUID = -5155395614743692362L;

	private Long idAnag;
	private String annoDocumento;
	private String tipologia;

	public Long getIdAnag() {
		return idAnag;
	}
	public void setIdAnag(Long idAnag) {
		this.idAnag = idAnag;
	}
	public String getAnnoDocumento() {
		return annoDocumento;
	}
	public void setAnnoDocumento(String annoDocumento) {
		this.annoDocumento = annoDocumento;
	}
	public String getTipologia() {
		return tipologia;
	}
	public void setTipologia(String tipologia) {
		this.tipologia = tipologia;
	}


}
