package it.csi.sigas.sigasbl.request.home;

import java.io.Serializable;

public class DownloadDocumentazioneReport implements Serializable{

	private static final long serialVersionUID = 1L;

	private Long id_anag;
    private String anno;
    private Long id_tipo_comunicazione;
    
	public Long getId_anag() {
		return id_anag;
	}
	public void setId_anag(Long id_anag) {
		this.id_anag = id_anag;
	}
	public String getAnno() {
		return anno;
	}
	public void setAnno(String anno) {
		this.anno = anno;
	}
	public Long getId_tipo_comunicazione() {
		return id_tipo_comunicazione;
	}
	public void setId_tipo_comunicazione(Long id_tipo_comunicazione) {
		this.id_tipo_comunicazione = id_tipo_comunicazione;
	}
	
	
}
