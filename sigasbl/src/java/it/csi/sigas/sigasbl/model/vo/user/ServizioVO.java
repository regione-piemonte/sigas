package it.csi.sigas.sigasbl.model.vo.user;

import it.csi.sigas.sigasbl.common.rest.VO;

public class ServizioVO implements VO {

	private static final long serialVersionUID = 5137921454586498490L;

	private Long id;
	private String descrizione;
	private boolean checked;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	public boolean isChecked() {
		return checked;
	}

	public void setChecked(boolean checked) {
		this.checked = checked;
	}

	@Override
	public String toString() {
		return "ServizioVO [id=" + id + ", descrizione=" + descrizione + "]";
	}

}
